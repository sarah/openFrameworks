//--------------------------------------------------------------
void ofApp::draw(){
    
    // animated circle to ellipse
    ofSetBackgroundColor(0);
    ofNoFill();
    float sinOfTime = sin(ofGetElapsedTimef()*1.1);
    float sinOfTimeRemapped = ofMap(sinOfTime,-1,1,180,500);
    ofDrawEllipse(ofGetWidth()/2, ofGetHeight()/2, sinOfTimeRemapped, 500);
}

